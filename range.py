#Range
'''Ele pode ser chamado de maneira simples, apenas com um argumento. Nesse caso, a sequência começará em 0 e 
será incrementada de uma unidade até o limite do parâmetro passado (exclusive). Por exemplo, range(3) cria
a sequência (0, 1, 2).'''

for i in range(1,11):
    print(i)

for i in range(30,60,3):
    print(i)
